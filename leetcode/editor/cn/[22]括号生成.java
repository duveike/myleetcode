class Solution {
    public List<String> generateParenthesis(int n) {
        List<String> res = new ArrayList<>();
        dfs("", res, 0, 0, n);
        return res;

    }

    private void dfs(String str,List<String> res, int left, int right, int n) {
        if(left>n) return;
        if(right>=n) {
            res.add(str);
        }
        dfs(str+"(", res, left+1,right,n);
        if(right<left) {
            dfs(str+")", res, left, right+1, n);
        }
    }
}
